      curtain.style.transition = `opacity ${INTRO_TIMING.reducedMotionCurtainFadeMs}ms ease-out`;
      curtain.style.opacity = "0";
      onHandoff();
      onComplete();
      const t = window.setTimeout(() => {
        setPhase("done");
      }, INTRO_TIMING.reducedMotionTotalMs);
      return () => window.clearTimeout(t);
